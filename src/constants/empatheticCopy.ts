// Helper function to get personalized copy
export const getEmpatheticCopy = (
  category: keyof typeof EMPATHETIC_COPY,
  key: string,
  variables?: Record<string, string | number>
): string => {
  const copy = EMPATHETIC_COPY[category]?.[key as any] || key;
  
  if (!variables) return copy;
  
  // Replace variables in copy
  return Object.entries(variables).reduce((text, [varKey, value]) => {
    return text.replace(new RegExp(`\\{${varKey}\\}`, 'g'), String(value));
  }, copy);
};
